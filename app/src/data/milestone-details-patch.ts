/**
 * 将深度叙事补丁注入到事件数组中
 * 遍历事件，如果事件 ID 在补丁中且事件没有已有 details，则注入
 */
export function injectMilestoneDetails(events: HistoricalEvent[]): HistoricalEvent[] {
  return events.map(event => {
    if (event.details?.trim()) return event
    const patch = milestoneDetailsPatch[event.id]
    if (!patch) return event
    return { ...event, details: patch }
  })
}
