export function injectImportantDetails(events: HistoricalEvent[]): HistoricalEvent[] {
  return events.map(event => {
    if (event.details?.trim()) return event
    const patch = importantDetailsPatch[event.id]
    if (!patch) return event
    return { ...event, details: patch }
  })
}
