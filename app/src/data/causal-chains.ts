/**
 * 将因果链注入到事件数组中。
 * 链中相邻事件互相建立 relatedIds 关联。
 * 已有的 relatedIds 会被保留并合并。
 */
export function injectCausalChains(events: HistoricalEvent[]): HistoricalEvent[] {
  // 构建 id → 关联ID集合 的映射
  const relMap = new Map<string, Set<string>>()

  for (const chain of CAUSAL_CHAINS) {
    for (let i = 0; i < chain.length; i++) {
      const id = chain[i]
      if (!relMap.has(id)) relMap.set(id, new Set())
      const set = relMap.get(id)!
      // 关联前一个
      if (i > 0) set.add(chain[i - 1])
      // 关联后一个
      if (i < chain.length - 1) set.add(chain[i + 1])
    }
  }

  // 注入到事件
  return events.map(event => {
    const chainRels = relMap.get(event.id)
    if (!chainRels || chainRels.size === 0) return event

    // 合并已有的 relatedIds
    const existing = new Set(event.relatedIds ?? [])
    chainRels.forEach(id => {
      if (id !== event.id) existing.add(id)
    })

    return {
      ...event,
      relatedIds: Array.from(existing),
    }
  })
}
